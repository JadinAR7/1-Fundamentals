# This for loop will print the numbers from 20 to 11 in steps of -2.
for i in range(20,11,-2):
    print(i)
print('The loop has finished')

# range(0,10,2) generates a sequence of numbers from 20 to 11 in steps of -2.
#First number is the starting number, second number is the ending number and the third number is the step size.